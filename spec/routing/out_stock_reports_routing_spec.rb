require "rails_helper"

RSpec.describe OutStockReportsController, type: :routing do
  describe "routing" do
    it "routes to #index" do
      expect(get: "/out_stock_reports").to route_to("out_stock_reports#index")
    end

    it "routes to #show" do
      expect(get: "/out_stock_reports/1").to route_to("out_stock_reports#show", id: "1")
    end


    it "routes to #create" do
      expect(post: "/out_stock_reports").to route_to("out_stock_reports#create")
    end

    it "routes to #update via PUT" do
      expect(put: "/out_stock_reports/1").to route_to("out_stock_reports#update", id: "1")
    end

    it "routes to #update via PATCH" do
      expect(patch: "/out_stock_reports/1").to route_to("out_stock_reports#update", id: "1")
    end

    it "routes to #destroy" do
      expect(delete: "/out_stock_reports/1").to route_to("out_stock_reports#destroy", id: "1")
    end
  end
end
